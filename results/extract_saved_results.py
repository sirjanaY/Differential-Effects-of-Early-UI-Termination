#!/usr/bin/env python3
"""Extract saved notebook outputs into a stable Markdown snapshot."""

from __future__ import annotations

import argparse
import json
from collections import OrderedDict
from datetime import datetime, timezone
from pathlib import Path


NOTEBOOKS = OrderedDict(
    [
        (
            "notebooks/SignificanceHolzerStyle.ipynb",
            "Recommended primary notebook: strongest saved DDD, placebo, and leave-one-state-out outputs.",
        ),
        (
            "archive/notebooks/SignificanceHolzerStyle.ipynb",
            "Root-level variant with overlapping DDD results and similar event-study logic.",
        ),
        (
            "baseline_model.ipynb",
            "Baseline TWFE reference notebook.",
        ),
        (
            "archive/notebooks/FirstValidTimeFrame.ipynb",
            "Exploratory sensitivity notebook with many alternative specifications.",
        ),
        (
            "archive/notebooks/Significant.ipynb",
            "Large exploratory robustness notebook.",
        ),
        (
            "archive/notebooks/SignificantBADTIMEFRAME.ipynb",
            "Exploratory variant with a problematic timeframe, based on filename and notebook role.",
        ),
        (
            "archive/notebooks/BAD_BASELINE.ipynb",
            "County TWFE notebook with weak saved baseline output.",
        ),
        (
            "archive/notebooks/Untitled-2.ipynb",
            "Scratch or intermediate exploratory notebook.",
        ),
    ]
)

KEYWORDS = [
    "Key Coefficient (TreatState:Post:LowWage)",
    "Key Coefficient (TreatState:Post)",
    "TreatState:Post:LowWage  : Coef",
    "TreatState:Post          : Coef",
    "Policy effect (Policy_DiD coefficient)",
    "Policy_DiD",
    "Summary of Baseline Policy Effects",
    "Insignificant",
    "R-squared:",
    "PLACEBO TEST",
    "Parallel Trends Assumption",
    "Result:",
]

SKIP_SUBSTRINGS = [
    "non-null",
    "float64",
    "object",
    "int64",
    "year month day",
]


def notebook_output_text(path: Path) -> str:
    data = json.loads(path.read_text())
    chunks: list[str] = []
    for cell in data.get("cells", []):
        for output in cell.get("outputs", []):
            if "text" in output:
                text = output["text"]
                chunks.append("".join(text) if isinstance(text, list) else str(text))
            if "data" in output and "text/plain" in output["data"]:
                text = output["data"]["text/plain"]
                chunks.append("".join(text) if isinstance(text, list) else str(text))
    return "\n".join(chunks)


def interesting_lines(text: str, limit: int = 14) -> list[str]:
    lines = []
    seen = set()
    for raw_line in text.splitlines():
        line = " ".join(raw_line.split())
        if not line:
            continue
        if any(token in line for token in SKIP_SUBSTRINGS):
            continue
        if any(keyword in line for keyword in KEYWORDS):
            if line not in seen:
                seen.add(line)
                lines.append(line)
        if len(lines) >= limit:
            break
    return lines


def loo_block(text: str, max_rows: int = 8) -> list[str]:
    target = "--- Summary of LOO Coefficients ---"
    if target not in text:
        return []
    lines = text.splitlines()
    start = None
    for idx, line in enumerate(lines):
        if target in line:
            start = idx + 1
            break
    if start is None:
        return []

    block: list[str] = []
    for line in lines[start:]:
        normalized = " ".join(line.split())
        if not normalized:
            if block:
                break
            continue
        block.append(normalized)
        if len(block) >= max_rows + 1:
            break
    return block


def csv_files(root: Path, relative_dir: str) -> list[str]:
    directory = root / relative_dir
    if not directory.exists():
        return []
    files = []
    for path in sorted(directory.glob("*.csv")):
        size_kb = path.stat().st_size / 1024
        files.append(f"`{path.relative_to(root).as_posix()}` ({size_kb:.1f} KB)")
    return files


def generate_markdown(root: Path) -> str:
    generated = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    lines = [
        "# Research Snapshot",
        "",
        f"Generated by `scripts/extract_saved_results.py` on {generated}.",
        "",
        "This file summarizes saved notebook outputs already committed to the repo. It does not rerun models.",
        "",
        "## Data Assets Present",
        "",
        "### Raw CSV files",
    ]

    raw_csvs = csv_files(root, "data/raw")
    if raw_csvs:
        lines.extend([f"- {entry}" for entry in raw_csvs])
    else:
        lines.append("- None found.")

    lines.extend(
        [
            "",
            "### Processed CSV files",
        ]
    )

    processed_csvs = csv_files(root, "data/processed")
    if processed_csvs:
        lines.extend([f"- {entry}" for entry in processed_csvs])
    else:
        lines.append("- None found.")

    lines.extend(
        [
            "",
            "## Notebook Inventory",
            "",
            "| Notebook | Current role |",
            "| --- | --- |",
        ]
    )
    for rel_path, note in NOTEBOOKS.items():
        if (root / rel_path).exists():
            lines.append(f"| `{rel_path}` | {note} |")

    lines.extend(["", "## Saved Output Highlights", ""])

    for rel_path, note in NOTEBOOKS.items():
        path = root / rel_path
        if not path.exists():
            continue
        text = notebook_output_text(path)
        extracted_lines = interesting_lines(text)
        loo_lines = loo_block(text)

        lines.extend([f"### `{rel_path}`", "", f"- Role: {note}"])
        if extracted_lines:
            lines.append("- Highlighted saved lines:")
            lines.extend([f"  - {line}" for line in extracted_lines])
        else:
            lines.append("- Highlighted saved lines: none matched the extraction rules.")

        if loo_lines:
            lines.append("- Leave-one-state-out excerpt:")
            lines.append("")
            lines.append("```text")
            lines.extend(loo_lines)
            lines.append("```")

        lines.append("")

    lines.extend(
        [
            "## Immediate Research-Readiness Priorities",
            "",
            "1. Freeze one canonical notebook or script path for the main claim.",
            "2. Normalize data paths so replication does not depend on `~/Downloads`.",
            "3. Export final tables and figures into tracked outputs rather than relying only on notebook cell outputs.",
            "4. Pin package versions once the canonical pipeline is stable.",
        ]
    )

    return "\n".join(lines) + "\n"


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--output",
        default="data/outputs/research_snapshot.md",
        help="Path to the Markdown output file.",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    root = Path(__file__).resolve().parent.parent
    output_path = root / args.output
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(generate_markdown(root))
    print(f"Wrote {output_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
